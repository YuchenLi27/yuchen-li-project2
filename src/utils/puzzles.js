function deepCopyBoard(board) {
  return board.map((row) => [...row])
}

export function getRandomPuzzle(mode) {
  const list = mode === 'easy' ? EASY_PUZZLES : NORMAL_PUZZLES
  const randomIndex = Math.floor(Math.random() * list.length)
  const selected = list[randomIndex]

  return {
    puzzle: deepCopyBoard(selected.puzzle),
    solution: deepCopyBoard(selected.solution),
  }
}